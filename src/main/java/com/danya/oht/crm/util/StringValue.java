package com.danya.oht.crm.util;

public class StringValue {

    private String value;

    public StringValue() {
        value = "";
    }

    public StringValue(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }
}
